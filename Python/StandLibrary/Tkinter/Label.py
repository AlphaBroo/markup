"""
Label标签
"""
# 用于在窗口中显示文本或位图
# width         --->宽度
# height        --->高度
# compound      --->指定文本与图像如何在Label上显示，缺省为None，当指定image/bitmap时，文本将被覆盖，只显示图像。--->left,right,top,bottom,center
# wraplength    --->指定多少单位后开始换行，用于多行显示文本
# justify       --->指定多行的对齐方式--->left,right
# anchor        --->指定文本或图象在Label上显示位置，对应于东南西北及四角---> n,s,e,w,nw,sw,se,ne,center(默认)
# image / bm    --->显示自定义图片，如png,gif
# bitmap        --->显示内置的位图


# 实现选项的单选功能

# 创建对象
# Radiobutton对象 = Radiobutton(Windows窗口对象，text = Radiobutton组件显示的文本)

# 显示对象
# Radiobutton对象.pack()


# 属性
# variable      --->单选按钮索引变量，通过变量的值确定哪个单选按钮被选中。一组单选按钮使用同一个索引变量
# value         --->单选按钮选中时变量的值
# command       --->单选按钮讯重视执行的命令(函数)

# 方法
# deselect()    --->取消选择
# select()      --->选择
# invoke()      --->调用单选按钮command指定的回调函数
















































# 创建和显示Entry对象
# Entry对象 = Entry(Windows窗口对象)

# 显示Entry对象的方法
# Entry对象.pack()

# 获取Entry组件的内容
# get()

# Entry属性
# show                  --->若设置为字符*，则输入文本框内显示为*，用于密码输入
# insertbackground      --->插入光标的颜色，默认设置为黑色black
# selectbackground      --->选中文本的背景色
# selectforeground      --->选中文本的前景色
# width                 --->组件的宽度(所占字符个数)
# fg                    --->字体前景颜色
# bg                    --->背景颜色
# state                 --->设置组件状态--->normal(默认),disabled,readonly
